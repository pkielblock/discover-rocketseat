// Array

const animals = [
    'Lion',
    'Moneky',
    {
        name: 'Cat',
        age: 3
    }
];

// Acessar Todos os Valores
console.log(animals);

// Acessar um valor especifico do array
console.log(animals[0]);

// Saber o tamanho do array
console.log(animals.length);

// Retorna um objeto dentro do array
console.log(animals[2])

// Retorna uma propriedade do objeto dentro do array
console.log(animals[2].name)